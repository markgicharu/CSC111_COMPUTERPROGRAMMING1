//This program executes the while loop for the hello world

//preprocessor directive
#include <iostream>
using namespace std;

//main functions begins here
int main()
{
  //declare variables
  int counter = 1;
  
  //execute do while loop
  
  
    do {cout<<"Hello World!"<<endl;}
    while(counter<=10);
    counter ++;
  
  //terminate the program
  return 0;
  
}
